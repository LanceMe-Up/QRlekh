import { ApiProperty } from '@nestjs/swagger';
import { IsBoolean, IsNumber, IsOptional, IsString } from 'class-validator';

export class SubQrDto {
  @IsString()
  @ApiProperty({ example: 'Mai Temple', description: 'Known For' })
  knownFor: string;

  @IsString()
  @ApiProperty({ example: 'Temple', description: 'Name of Qrlekh Info' })
  title: string;

  @IsString()
  @IsOptional()
  @ApiProperty({
    example: 'temple',
    description: 'should be generated by name and can be unique',
  })
  slug: string;

  @IsNumber()
  @IsOptional()
  @ApiProperty()
  visitor: number;

  @IsString()
  @ApiProperty({
    example: 'Temple ',
    description: 'Give the short detail of Temple?',
  })
  desc: string;

  @IsBoolean()
  @IsOptional()
  @ApiProperty({
    default: false,
  })
  isFeature: boolean;

  // @IsNumber()
  // @IsOptional()
  // @ApiProperty({ minLength: 1, maxLength: 5, default: 1 })
  // rating: number;

  @IsOptional()
  @ApiProperty()
  like: number[];

  @IsNumber()
  qrlekhDataId: number;
}
